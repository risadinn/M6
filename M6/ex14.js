//Remover/desativar breakpoints:

//Para remover todos de uma vez: no painel Sources/Debugger abra a lista de Breakpoints (barra lateral) → clique com o botão direito e escolha "Remove all breakpoints" (ou use o menu de três pontos e escolha a opção equivalente).
//Para apenas desativar temporariamente sem apagar: use o botão "Deactivate breakpoints" (ícone de pausa com um traço) na barra de ferramentas do depurador — isso desliga todos os breakpoints até reativar.
//Essas ações estão disponíveis em Chrome/Edge/Firefox (em Safari use o Web Inspector → Debugger e procure opções equivalentes).